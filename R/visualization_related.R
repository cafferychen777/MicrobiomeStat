##' @rdname as-grob
##' @method as.grob expression
##' @noRd
as.grob.expression <- function(plot, ...) {
  p <- tryCatch(base2grob(plot, ...),
                error = function(e) NULL)

  if (is.null(p)) {
    p <- grid2grob(plot_fun(plot, ...)())
  }

  return(p)
}

